package dev.tamboui.widgets.list;

/**
 * Direction for rendering list items.
 */
public enum ListDirection {
    /**
     * Render items from top to bottom (default).
     */
    TOP_TO_BOTTOM,

    /**
     * Render items from bottom to top.
     */
    BOTTOM_TO_TOP
}
